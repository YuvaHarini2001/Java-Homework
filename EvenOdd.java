class EvenOdd
{
	public static void main(String[] args)
	{
		int a = 26;
		int b = 35;
		
		if(a%2==0)
		{
			System.out.println("Even Number:"+a);
		}
		else 
		{
			System.out.println("Odd Number:"+a);
		}
		if(b%2==0)
		{
			System.out.println("Even Number:"+b);
		}
		else
		{
			System.out.println("Odd Number:"+b);
		}
	}
}