class PositiveNegativeNeutral
{
	public static void main(String[] args)
	{
		int a = 57;
		int b = -78;
		int c = 0;
		
		if(a>0)
		{
			System.out.println("Positive Number:"+a);
		}
		else 
		{
			System.out.println("Negative Number:"+a);
		}
		if(b<0)
		{
			System.out.println("Negative Number:"+b);
		}
		else
		{
			System.out.println("Neutral Number:"+b);
		}
		if(c==0)
		{
			System.out.println("Neutral Number:"+c);
		}
		else
		{
			System.out.println("Negative Number:"+c);
		}
	}
}