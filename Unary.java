class Unary
{
	public static void main(String[] args)
	{
		int A = 5;
		System.out.println("INITIAL VALUE:"+A);
		A++;
		System.out.println("AFTER A++:"+A);
		++A;
		System.out.println("AFTER ++A:"+A);
		A--;
		System.out.println("AFTER A--:"+A);
		--A;
		System.out.println("AFTER --A:"+A);
		
	}
	
}